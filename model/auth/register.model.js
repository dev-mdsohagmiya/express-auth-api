/** @format */
const { Schema, model } = require("mongoose");

const registerSchema = Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
});

module.exports = new model("users", registerSchema);
